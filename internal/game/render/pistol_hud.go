// BuiltinPistolHUDFrames — кадры A–D (idle + огонь), вшиты в бинарник без IWAD.
var BuiltinPistolHUDFrames []PistolHUDFrame

func init() {
	frames := []string{pistolAsciiA, pistolAsciiB, pistolAsciiC, pistolAsciiD}
	out := make([]PistolHUDFrame, 0, len(frames))
	for _, raw := range frames {
		lines := trimAsciiBox(raw)
		if len(lines) == 0 {
			continue
		}
		fr := parsePistolHUDFrame(lines)
		if len(fr.Chars) == 0 {
			continue
		}
		out = append(out, cropFrameH(fr, 0, 0))
	}
	for len(out) < 4 && len(out) > 0 {
		out = append(out, out[len(out)-1])
	}
	BuiltinPistolHUDFrames = out
}

