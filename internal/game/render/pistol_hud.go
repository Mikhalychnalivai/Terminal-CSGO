func pistolHUDGlyph(ch rune) (rune, uint32) {
	if ch == ' ' || ch == '\t' || ch == 0 {
		return 0, 0
	}
	switch ch {
	case '#':
		return ch, RGBPacked(18, 20, 32)
	case '%':
		return ch, RGBPacked(42, 48, 62)
	case '@':
		return ch, RGBPacked(88, 82, 78)
	case '*':
		return ch, RGBPacked(255, 220, 140)
	case '+':
		return ch, RGBPacked(120, 125, 140)
	case '|':
		return ch, RGBPacked(40, 44, 58)
	case '.', ',', '\'':
		return ch, RGBPacked(95, 62, 42)
	case '`':
		return ch, RGBPacked(88, 58, 40)
	case '-', '_', '=':
		return ch, RGBPacked(55, 50, 45)
	case ':', ';':
		return ch, PistolHUDBrownBeigeBlend(2)
	case '<', '>', '[', ']', '(', ')':
		return ch, PistolHUDBrownBeigeBlend(3)
	case '{', '}':
		return ch, PistolHUDBeige
	case '^', '~':
		return ch, RGBPacked(200, 190, 175)
	case '\\', '/':
		return ch, PistolHUDBrownBeigeBlend(1)
	default:
		return ch, RGBPacked(45, 48, 58)
	}
}

func parsePistolHUDFrame(lines []string) PistolHUDFrame {
	h := len(lines)
	if h == 0 {
		return PistolHUDFrame{}
	}
	rows := make([][]rune, h)
	maxW := 0
	for i, line := range lines {
		rows[i] = []rune(line)
		if len(rows[i]) > maxW {
			maxW = len(rows[i])
		}
	}
	fr := PistolHUDFrame{
		Chars: make([][]rune, h),
		RGB:   make([][]uint32, h),
	}
	for y := 0; y < h; y++ {
		fr.Chars[y] = make([]rune, maxW)
		fr.RGB[y] = make([]uint32, maxW)
		for x := 0; x < maxW; x++ {
			var ch rune = ' '
			if x < len(rows[y]) {
				ch = rows[y][x]
			}
			r, c := pistolHUDGlyph(ch)
			fr.Chars[y][x] = r
			fr.RGB[y][x] = c
		}
	}
	return fr
}
